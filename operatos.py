#operators in python.
#what are operators?
#operators tell a computer what to do with an operand
#an operand is what an operator acts upon
#these operators are categorized.
#arithmetic operators (+,-,*,/,//,%,*)
#examples
num1 = 10
num2 = 20
print (num1 + num2)
print (num1 - num2)
print (num1 * num2)
print (num1 / num2)
print (num1 // num2)
print (num1 % num2)
print (num1 ** num2)


